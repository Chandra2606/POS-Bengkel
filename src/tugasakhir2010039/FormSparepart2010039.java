    private void txtCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCariActionPerformed
        DefaultTableModel tabelModel = new DefaultTableModel();
        tabelModel.addColumn("Kode Sparepart");
        tabelModel.addColumn("Nama Sparepart");
        tabelModel.addColumn("Harga"); 
        tabelModel.addColumn("Stok");
        tabelModel.addColumn("Upah");
        try {
            koneksi();
            String sql = "select * from sparepart2010039 where namasp like '%"+ txtCari.getText() +"%'";
            ResultSet rs = stt.executeQuery(sql);
            while (rs.next()){
                tabelModel.addRow(new Object[]{
                    rs.getString("kodesp"),
                    rs.getString("namasp"),
                    rs.getString("harga"),
                    rs.getString("stok"),
                    rs.getString("ongkos")
                });
            }
            tabel.setModel(tabelModel);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan" + e);
        }
       
    }//GEN-LAST:event_txtCariActionPerformed

