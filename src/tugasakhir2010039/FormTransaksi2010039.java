    private void nofaktur()
    {
       try {
           koneksi();
          String sql="select * from service2010039 order by nofak desc";
            stt=conn.createStatement();
            rs=stt.executeQuery(sql);
            if (rs.next()) {
                String nofak = rs.getString("nofak").substring(1);
                String AN = "" + (Integer.parseInt(nofak) + 1);
                String Nol = "";

                if(AN.length()==1)
                {Nol = "000";}
                else if(AN.length()==2)
                {Nol = "00";}
                else if(AN.length()==3)
                {Nol = "0";}
                else if(AN.length()==4)
                {Nol = "";}

               txtnofak.setText("F" + Nol + AN);
            } else {
               txtnofak.setText("F0001");
            }

           }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
           }
     }
