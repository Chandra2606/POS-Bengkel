    private void txtqtyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtqtyActionPerformed
        // TODO add your handling code here:
                int qty = Integer.parseInt(txtqty.getText()); 
        int stok = Integer.parseInt(txtstok.getText());
        double harga = Double.parseDouble(txtharga.getText()); 
        double ongkos = Double.parseDouble(txtongkos.getText());
        double jmlHarga;
        if (stok >= qty) {
        jmlHarga = qty * harga + ongkos; 
        txtjumlah.setText(String.valueOf(jmlHarga));
        btntambah.requestFocus();
        
        }else {
        JOptionPane.showMessageDialog(null, "Stok Tidak Cukup");
        txtqty.setText("");
        }
    }//GEN-LAST:event_txtqtyActionPerformed

    private void txtqtyKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtqtyKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==KeyEvent.VK_ENTER) {
            int jumlah = Integer.parseInt(txtqty.getText());
            int stok = Integer.parseInt(txtstok.getText());
            if(evt.getKeyCode()==KeyEvent.VK_ENTER){
                if (stok < jumlah ){
                    JOptionPane.showMessageDialog(null,"Stok Kurang");
                    txtqty.requestFocus();
                    txtqty.setText("");
                }else {
                    txtongkos.requestFocus();
                    tambahItem.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_txtqtyKeyPressed

